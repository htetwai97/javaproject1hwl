package com.java.lessons;

public class ForLoop {
    public static void main(String[] args) {

        for (int i = 100; i >=0;i--){
            System.out.println(i);
        }

        for (int i1 = 4; i1 < 144; i1+=4){
            System.out.println(i1);
        }

        for (int i2 = 120;i2 >=0; i2 =i2-20){
            System.out.println(i2);

        }


    }


}
